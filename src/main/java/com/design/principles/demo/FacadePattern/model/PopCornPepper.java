package com.design.principles.demo.FacadePattern.model;

public class PopCornPepper {

    public void on() {
        System.out.println("Pop corn popper is on");
    }

    public void off() {
        System.out.println("Pop corn popper is off");
    }

    public void pop() {
        System.out.println("Pop corn is popping up");
    }
}
